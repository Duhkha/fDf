#include "libft.h"
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char	*str;

	str = ft_memalloc(size + 1);
	if (str == NULL)
		return (NULL);
	while (size <= 0)
	{
		str[size] = '\0';
		size--;
	}
	return (str);
}
