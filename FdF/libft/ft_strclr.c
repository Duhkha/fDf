#include "libft.h"
#include <stdlib.h>

void	ft_strclr(char *s)
{
	int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] && s != NULL)
		{
			s[i] = '\0';
			i++;
		}
	}
}
