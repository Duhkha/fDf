#include "libft.h"
#include <unistd.h>

void	ft_putstr_fd(const char *c, int fd)
{
	int i;

	i = 0;
	if (c != NULL)
	{
		while (c[i])
		{
			write(fd, &c[i], 1);
			i++;
		}
	}
}
